    private void buatTable(){
        model = new DefaultTableModel();
        model.addColumn("Kode Tiket");
        model.addColumn("Nama Tiket");
        model.addColumn("Satuan");
        model.addColumn("Stok");
        model.addColumn("Harga Kontrak");
        model.addColumn("Harga Beli");
        Tabel1.setModel(model);
        
    }
    
    private void showTable(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListTiket.clear();
        ListTiket=tc.tampil();
        for (x=0; x<ListTiket.size(); x++){
            Object[] data=new Object[6];
            data[0]=ListTiket.get(x).getKodetiket();
            data[1]=ListTiket.get(x).getNmtiket();
            data[2]=ListTiket.get(x).getSatuan();
            data[3]=ListTiket.get(x).getJmlstok();
            data[4]=ListTiket.get(x).getHrgkontrak();
            data[5]=ListTiket.get(x).getHrgbeli();
            model.addRow(data);
        
     }
    }
    
    private void buatKdBarang(){
        ListTiket=tc.tampil();
        int a=ListTiket.size()-1;
        int no=Integer.parseInt(ListTiket.get(a).getKodetiket().replace("A-",""))+1;
        kdtiket.setText("A-"+no);
        kdtiket.setEnabled(false);
    }
    
