    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if (nmtiket.getText().isEmpty() || cmbsatuan.getSelectedItem().equals("") ||
                tstok.getText().isEmpty() || hrgkontrak.getText().isEmpty() || hrgbeli.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Data Harus Lengkap","Aplikasi Tiket", JOptionPane.INFORMATION_MESSAGE);
        }else{
            DT.setKodetiket(kdtiket.getText());
            DT.setNmtiket(nmtiket.getText());
            DT.setsatuan(cmbsatuan.getSelectedItem().toString());
            DT.setJmlstok(Integer.parseInt(tstok.getText()));
            DT.setHrgkontrak(Integer.parseInt(hrgkontrak.getText()));
            DT.setHrgbeli(Integer.parseInt(hrgbeli.getText()));
            if(btntambah.getText().equalsIgnoreCase("Batal")){
                if(tc.tambahtiket(DT)==1){
                    JOptionPane.showMessageDialog(null,"Data telah disimpan","Aplikasi Tiket", JOptionPane.INFORMATION_MESSAGE);
                    buatTable();
                    showTable();
                    
                }else{
                    JOptionPane.showMessageDialog(null,"Data gagal disimpan","Aplikasi Tiket", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            if(btnedit.getText().equalsIgnoreCase("Batal")){
                if(tc.editTiket(DT)==1){
                    JOptionPane.showMessageDialog(null,"Data telah dirubah","Aplikasi Tiket", JOptionPane.INFORMATION_MESSAGE);
                    buatTable();
                    showTable();
                    
                }else{
                    JOptionPane.showMessageDialog(null,"Data gagal dirubah","Aplikasi Tiket", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            bersih();
            siapIsi(false);
            btntambah.setText("Tambah");
            btnedit.setText("Edit");
            tombolNormal();
            
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        if (btntambah.getText().equalsIgnoreCase("Tambah")){
            btntambah.setText("Batal");
            bersih();
            siapIsi(true);
            buatKdBarang();
            nmtiket.requestFocus();
            tombolKembali();
        }else{
            btntambah.setText("Tambah");
            bersih();
            siapIsi(false);
            tombolNormal();
        }
    }//GEN-LAST:event_btntambahActionPerformed

    private void Tabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabel1MouseClicked
       int baris = Tabel1.getSelectedRow();
       
       kdtiket.setText(Tabel1.getModel().getValueAt(baris, 0).toString());
       nmtiket.setText(Tabel1.getModel().getValueAt(baris, 1).toString());
       cmbsatuan.setSelectedItem(Tabel1.getModel().getValueAt(baris, 2).toString());
       tstok.setText(Tabel1.getModel().getValueAt(baris, 3).toString());
       hrgkontrak.setText(Tabel1.getModel().getValueAt(baris, 4).toString());
       hrgbeli.setText(Tabel1.getModel().getValueAt(baris, 5).toString());
       siapIsi(false);
       btnhapus.setEnabled(true);
       btnedit.setEnabled(true);
       
    }//GEN-LAST:event_Tabel1MouseClicked

